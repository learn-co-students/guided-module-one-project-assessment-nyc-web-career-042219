  def loading_bar
    puts 'LOADING...'
    spinner = Enumerator.new do |e|
      loop do
        e.yield '|'
        e.yield '/'
        e.yield '-'
        e.yield '\\'
      end
    end

    1.upto(100) do |i|
      progress = '=' * (i/5) unless i < 5
      printf("\rCombined: [%-20s] %d%% %s", progress, i, spinner.next)
      sleep(0.1)
    end
  end

